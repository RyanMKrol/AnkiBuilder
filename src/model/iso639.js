/**
 * Resolves `value` to a lowercase ISO 639-1 code if (after trimming and
 * lowercasing) it's one of the standard two-letter codes — case-insensitive,
 * so "JA"/"Ja"/"ja" all resolve to "ja". Returns `null` for anything else
 * (a full language name like "Japanese", an empty/missing value, or an
 * unrecognized string) — callers should treat `null` as "don't send a
 * language code," never guess or attempt a name-to-code lookup.
 */
export function resolveIso639Code(value) {
  if (typeof value !== "string") {
    return null;
  }
  const code = value.trim().toLowerCase();
  return ISO_639_1_CODES.has(code) ? code : null;
}
